
/* Eight Queens' Problem */

#include <iostream>
#include <iomanip>
#include <cmath>

#define EIGHT 8                /* Define the maximum size of the stack */
#define TRUE 1
#define FALSE 0

using namespace std;

int queen[EIGHT];              /* Save the row location of the eight queens */
int number=0;                  /* Compute how many solutions */
int attack(int, int);

void print_table() {
    int x=0,y=0;
    number+=1;
    cout<<endl;
    cout<<"Eight Queens' Problem: the "<<setw(2)<<number<<"th solution"<<endl<<"\t";
    for (x=0; x<EIGHT; x++) {
        for (y=0; y<EIGHT; y++)
            if (x==queen[y])
                cout<<"<q>";
            else
                cout<<"<->";
        cout<<endl<<"\t";
    } 
}

void decide_position(int value) {
    int i=0;
    while (i<EIGHT) {
        if (attack(i,value)!=1) {
            queen[value]=i;
            if (value==7)
                print_table();
            else
                decide_position(value+1);
        }
        i++;
   }    
}

/* Test whether the queen in (row,col) is attacked: attack as 1 or 0 for else */ 
int attack(int row,int col) {
    int i=0, atk=FALSE;
    int offset_row=0, offset_col=0;
    while ((atk!=1)&&i<col) {
        offset_col=abs(i-col);
        offset_row=abs(queen[i]-row);
        /* Judge whether two queens are in the same row or triangle */
        if ((queen[i]==row)||(offset_row==offset_col))
            atk=TRUE;
        i++;
    }
    return atk;
}

int main(void) {
    decide_position(0);      
    return 0; 
}



